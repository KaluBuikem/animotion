<h1>Introduction</h1>

<h2>What is Animotion?</h2>

<iframe width="560" height="315" src="https://www.youtube.com/embed/dBfZjv-lt2k" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe>

<p>
	<b>Animotion</b> is a tool for making presentations from slides to code explainers using web technologies
	you know and love.
</p>

<p>	
	It uses <a href="https://svelte.dev/">Svelte</a>,
	<a href="https://revealjs.com/">Reveal.js</a>
	and <a href="https://tailwindcss.com/">Tailwind CSS</a> to create beautiful animated presentations
	and visualizing ideas with code.
</p>

<iframe width="560" height="315" src="https://www.youtube.com/embed/4Lom_lqSGoY" title="YouTube video player" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" allowfullscreen></iframe>

<p>
	The main motivation behind <b>Animotion</b> was to make a learning tool to visualize ideas with code for videos inspired by <a href="https://motioncanvas.io/">Motion Canvas</a> but tailored to my needs as I couldn't customize any other tool how I wanted.
</p>

<p>
	While <b>Animotion</b> uses Reveal.js as the base it builds on top of it and improves the user experience.
</p>

